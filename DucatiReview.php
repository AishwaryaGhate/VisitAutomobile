		 	The 2018 version of the Ducati Monster 821 has arrived in India carrying a price tag of Rs 9.51 lakhs (ex-showroom, India). It directly competes with the Triumph Street Triple S and the Yamaha MT-09. However, around the 821’s price tag, you could look at nine other options that the market offers. Here is what the list looks like.From Ducati’s stables itself, is the Scrambler Café Racer which sports a design language that harks back to racing motorcycles from the ‘60s. The bike is priced at Rs 9.32 lakhs. You could also have the Scrambler Desert Sled which is an off-road focused motorcycle equipped with 200mm suspension travel for the front and rear. It is priced at Rs 9.45 lakhs.There also is the Scrambler Mach 2.0, based on the Scrambler Icon and gets a white, orange and yellow paint scheme. The bike is priced at Rs 8.56 lakhs. All the Scramblers are powered by the same 803cc L-twin Desmodromic engine, which delivers 75bhp and 68Nm of torque, mated to a six-speed gearbox and final chain drive.For 2018, the Monster 821 has been updated with a streamlined body and twin-pipe exhaust along with modern touches like an LED headlamp and colour instrument cluster. The fuel lid clasp on the tank harks back to the original 1993 Monster M900. The Monster 821 retains its exotic cues like exposed trellis frame and muscular fuel tank which give the bike a raw character. The TFT colour display now includes a gear indicator and a fuel gauge. At the heart of the Monster 821 is the same liquid-cooled L-twin Testastretta engine, although it has been revised to comply with BS IV norms. The output is slightly lower at 107bhp and 86Nm of torque. The engine is mated to a six-speed transmission, with a slipper clutch and a quick shifter which allows clutchless upshifts and downshifts. The electronics package includes three riding modes, a three-level ABS and an eight-level traction control system. 
