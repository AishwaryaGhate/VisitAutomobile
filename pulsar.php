	    <font color="black" size="5px">After number of spy pictures doing the round of the internet, Bajaj Motorcycles has finally launched its first fully-faired motorcycle, the Pulsar RS 200 for the Indian market. Previously touted to be called as the Pulsar SS200, this bike has been the most anticipated launch from the company.Marketed as the fastest Pulsar yet, the Pulsar RS200 designed to be a compact sportsbike and features clip-on handlebars. Unlike other fully-faired motorcycle like the Yamaha YZF-R15, the RS200 doesn’t have as aggressive riding stance as of a super sport motorcycle. The riding position is slightly upright and is suitable for mild touring. The Pulsar RS200 is powered by same 200cc engine that powers the naked Pulsar NS200. However, this engine is fuel-injected and churns out 24.5bhp at 9,750rpm and maximum torque of 18.6Nm at 8,000rpm.For the first time, the Pune-based manufacturer is offering an ABS variant as well, along with a non-ABS variant. Cosmetically, the Pulsar RS200 is the most aggressive looking bike in Bajaj’s portfolio. While the projector lamp was first introduced in the Pulsar 220F, the RS200 gets dual projector. The overall design cues are quite muscular making it a very busy design.The exhaust design is quite neat and compliments the overall motorcycle design. The Bajaj Pulsar RS200 is available in two dual-tone paint schemes - grey/black and blue/white. It competes with the Yamaha YZF-R15 and the Honda CBR150R. The prices of the Bajaj Pulsar RS200 starts from Rs 1,21,894 (ex-showroom Delhi).<br><br>

