	    <font color="black" size="5px">The Chevrolet Cruze is a compact car that has been made by the Chevrolet division of General Motors since 2008. The nameplate has been used previously in Japan, for a version of a subcompact hatchback car produced under a joint venture with Suzuki from 2001 to 2007, and was based on the Suzuki Ignis.
Since 2009, the Cruze nameplate has designated a globally developed, designed, and manufactured four-door compact sedan—complemented by a five-door hatchback body variant (in 2010 it also replaced the Chevy Cobalt from 2011, and a station wagon in 2012. The Cruze was actually released earlier in 2008 to the South Korean market under the name Daewoo Lacetti Premiere until the phasing out of the Daewoo brand in favor of Chevrolet in 2011. In Australia, the model has been on sale since 2009 as the Holden Cruze. This new generation Cruze does not serve as a replacement for the Suzuki-derived Japanese market predecessor.<br><br>
<font color="red">First Generation :</font>In 2008, GM introduced the Cruze compact car, carrying the "J300" internal designation.[13] This J300 iteration serves as a replacement for the Chevrolet Cobalt, Daewoo Lacetti and Holden Astra—all unrelated cars. GM phased out production of the Cobalt and its badge engineered counterpart, the Pontiac G5 in 2010, just prior to the manufacturing of the Chevrolet Cruze was to commence. The first renderings of the Cruze were revealed by GM at a press conference on July 15, 2008,with the first official images released on August 21, 2008.Cruze production sites include Gunsan, Jeonbuk, South Korea;Saint Petersburg, Russia;Shenyang, China;and Halol, India; Hanoi, Vietnam since April 2010 in complete knock-down (CKD) form, Ust-Kamenogorsk, Kazakhstan from May 2010; Rayong, Thailand after December 2010.<br><br>

<font color="red">Second Generation :</font>In 2013, Chevrolet had announced a launch for the second generation Cruze in late 2015, after originally being scheduled to debut in late 2014. The delay is reportedly caused by engineering modifications. The model had been spotted covered in camouflage while being tested in February,and October 2013.Its supposed interior was also spotted, while graphical renders were presented by the Carscoops website. The model will be slightly longer than the first generation.The new model was first announced for the Chinese market on the 2014 Beijing Auto Show and went on sale in August 2014. The four-door sedan has a fastback-like sloping roofline and a low drag coefficient of 0.28 comes with a choice of a 1.4 L (1,399 cc) turbocharged direct injection engine with a power of 110 kW (150 hp) @ 5600 RPM and torque of 235 N⋅m (173 lb⋅ft) @1600–4000 RPM, which can be mated with a six-speed manual transmission or seven-speed Start/Stop enabled dual-clutch gearbox, or a 1.5 L (1,490 cc) direct injection engine with a power of 84 kW (113 hp) @ 5600 RPM and torque of 146 N⋅m (108 lb⋅ft) @ 6000 RPM mated to a six-speed Start/Stop-capable automatic transmission. Both engines come from the new GM Small Gasoline Engine family.<br><br>
