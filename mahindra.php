The Mahindra XUV500 (pronounced as XUV five double O) is a mid-size, sport utility vehicle produced by the Indian automobile company Mahindra & Mahindra. The XUV500 was designed and developed at Mahindra's design and vehicle build center in Nashik and Chennai, and is manufactured in Mahindra's Chakan & Nashik plant, India. During its development, the car was code named 'W201'.It is the first monocoque chassis based vehicle the company has produced.
The XUV 500 was launched in September 2011, and by June 2012 it was available in 19 cities in India.To meet the growing demand for the XUV 500, M & M planned to increase the monthly production to 5000 units by Sept-Oct 2012.
The XUV500 is less well known outside India than the Mahindra Scorpio. The XUV500 was relatively popular in the South African market with sales exceeding 1200 units by February 2013.<br><br>
<font color="red">First generation </font> The XUV500 was launched in 2 variants in 2011, sharing the same engine:
 the W6 in two-wheel drive only. The W6 includes a 6-inch monochrome infotainment display, two airbags, ABS with electronic brake-force distribution (EBD), and disc brakes on all wheels.the W8 which can be specified in two- or four-wheel drive. The W8 adds GPS navigation, six airbags, a touch screen, electronic stability program (ESP) with rollover mitigation, Hill Hold and Hill Descent control, alloy wheels, and leather upholstery.The company later added a W4 base variant in 2013.<br><br>
<font color="red">Second generation </font> On April 18, 2018, another
facelifted version of Mahindra XUV500 was released. The variants are:
 W5, W7, W9, W11, W11 (O), W11 (O) AWD (diesel manual)
 W7 AT, W9 AT, W11 AT, W11 (O) AT, W11 (O) AT AWD(diesel automatic)
 G AT(petrol automatic)<br><br>
