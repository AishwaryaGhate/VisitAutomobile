         	The petrol version of the successful new-gen Innova that was launched in the Indian market this year. If you are wondering why this variant when there have been only a paltry sum of petrol Innova buyers earlier? Well, this is Toyota's answer to the ban in Delhi-NCR regions on diesel vehicles with more than 2,000cc engine capacity. The petrol model enabled the carmaker to sell the MPV in these regions.Sure, the ban has been lifted now and the Innova sales have been given the green light, but the petrol Innova still becomes a strong case in point for urban buyers who don’t have much running. As far as looks are concerned, the 2.7 ZX AT is cosmetically exactly identical to its diesel counterpart. So does it make sense? We tested it on performance and fuel efficiency parameters to give you a definitive answer.We didn't mention about the fuel efficiency figures earlier as they are pretty dismal. In fact, this can prove to be a deal breaker. We managed to get around 10.4kmpl on the highway and 7.9kmpl within the city. But we believe the fuel economy criteria can take a back seat for the engine’s higher refinement levels.So if you are looking for a MPV with loads of space, good enough to ferry around six people comfortably and don’t really have a high daily usage, this one makes sense for you. But the biggest barrier can be the high ex-showroom price of Rs 19.62 lakh for this top trim despite being a package with high levels of luxury, refinement and performance.
