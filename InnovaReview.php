		 	It looks like the exclusive design bits on the Touring Sport have done the trick with the Innova faithful. So, what do you get? For starters, there are extra trim bits over and above the range topping 2.8 ZX AT variant, on which the Touring Sport is based, namely the smoked headlamps along with a chrome lined bumper lip. The grille also gets contrast black and chrome treatment while the alloys get a matte black finish to complement the black peripheral cladding.Inside, the Touring Sport looks and feels, wait for it, like the Innova Crysta. There’s that instant familiarity you would expect, that is both a positive and a negative. Positive, because you know what you are going to get and negative, well, because this cabin isn’t entirely premium looking or feeling. It’s not any different to drive or packed with extra features over the standard car, but where the Touring Sport does work well is in pure design sense. Just look at the number of cosmetically-modified Innova Crystas (or even the older gen model, for that matter) you see on the road. The Touring Sport is a good-looking vehicle and it manages to stand out in the sea of standard Innova Crystas getting around. Buyers are likely to be attracted to that, especially those who have been considering the standard Innova Crysta and wanting to jazz it up a bit.
