	    <font color="black" size="5px">It may be the highest-selling two-wheeler in the Indian market, but Honda hasn’t stopped the evolution of the Activa. The latest set of updates that make up the 5G are more than merely cosmetic. The front apron gets chrome accents, but the LED headlamp will draw your attention first. The top-spec Deluxe variant even gets a digital display below the analogue speedometer; it now shows the fuel gauge, odometer and trip meter and a clock in the digital part of the instrument cluster. The Activa 5G also gets the new ignition key slot from the Grazia, which has the provision for the seat release.At the heart of the Activa 5G is the same 109cc engine which delivers 8bhp and 8.8Nm of torque. The engine is mated to a CVT transmission. It still rides on the archaic trailing-link front suspension and a rear monoshock. The brake setup consists of drum units at both ends with a combi braking system being offered as standard. A disc brake is not offered, even as an option.The Activa 5G is available in eight paint schemes – blue, white, red, silver, black, grey brown and yellow. It shares the market space with the TVS Jupiter, Hero Maestro Edge and the Yamaha Fascino. <br><br>

