		 A celebration and a smart marketing strategy. In a short span of just three years, TVS has put more one million Jupiter scooters on Indian roads. As a commemoration of this feat, the company rolled out this special edition variant, aptly christened Jupiter MillionR. What does the 'R' stand for? Not 'race' quite clearly. The only mechanical difference between the standard Jupiter and the Jupiter MillionR is the new front disc brake. So unless the decals and chrome bits add five horsepower, no, the Jupiter MillionR isn't any quicker off the mark. Nevertheless, we took it for a spin to find out what's new.Just like the standard TVS Jupiter, which in itself is a big compliment. The Jupiter MillionR is powered by the same single-cylinder engine which displaces 109cc, making 7.8bhp and 8Nm of torque. The power delivery is linear, with the engine's refinement and vibe-free mannerism being the highlight. However, the CVT transmission has been tuned keeping fuel economy in mind, which becomes apparent under hard acceleration. Even off the mark, there is noticeable lag before the power kicks in. On highways, the Jupiter MillionR feels comfortable cruising at 70kmph, leaving enough grunt for overtakes.Sure, it also gets a few other bells and whistles, but the disc brake is the piece de resistance of the TVS Jupiter MillionR. And then there is the colour. In a short span of just three years, the Jupiter has managed to carve out a niche in the Indian scooter market. In the current market scenario where the TVS Activa has demolished its competition (and the Hero Splendor, for that matter), the Jupiter is one of the few scooters which has managed to hold its own. But this also means that it has become too common. So if you want a Jupiter but are apprehensive about it getting lost in the crowd, the Jupiter MillionR is just the right variant for you. And given that the Jupiter MillionR is over Rs 5,000 more expensive than the standard variant, exclusivity is guaranteed. 
