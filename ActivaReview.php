		 <font color="black" size="4px">
		 	The current generation of scooters boast of all kinds of gizmos and hoo-haas to appeal to the current lot of consumers i.e. tech savvy youngsters. While the Honda Activa could snicker at the young buds with its near three lakh a month sales figure, it also needed to be in par with the crowd.So here in its fifth generation is the Activa 5G. It looks no different than its previous counterpart, save the front end. After all, the nine year-old design has seemed to work perfectly for Honda. And as they say, if it isn’t broken, don’t fix it!.The Activa 5G gets a full-LED headlamp along with chrome strips on the front apron. While you might not notice the chrome, the bright LED headlamp sure is attention grabbing as much as it is useful. It offers good projection and visibility even when not on high beam. And that’s about it if you opt for the standard version. On the DLX variant, in addition to the analogue speedometer you get a small digital unit, similar to that on the Grazia. One of the highest selling points of the Activa is its metal body, the finish of which is top notch.The Activa offers a neutral riding position credit to its flat and large floorboard. And while the seat may not be long, it is well cushioned and wide enough even for the heftier riders. The scooter gets a metal footboard as standard for a side saddled pillion.The Activa’s 109cc engine screams refinement like all other Hondas. The scooter seems undaunted even when threatened to go full-throttle, until it hits the 90kmph mark. That’s when you start to feel the grittiness of the otherwise butter-like smooth motor. The 8bhp and 9Nm producing engine is peppy too. The Activa was quick to pull away during our performance test even with a larger rider on board.The Honda Activa 5G retains the legacy of its previous counterparts in terms of reliability and brand value. While the design sure is dated, it has a widely appealing no-nonsense character. The Activa however, could do with a better braking and suspension setup which turns out to be the only drawback in performance. All-in-all the Activa 5G upholds its reputation of being a rugged scooter. 
