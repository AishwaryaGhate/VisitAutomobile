	    <font color="black" size="5px">The YZF-R15 changed the 150cc segment in the Indian market the way the CBZ did when it was launched. It was an everyday motorcycle that could genuinely be used as a trackday tool. The second version of the R15 traded practicality for more focused performance, but the advent of the KTM RC200 meant that much better performance was available for the sportbike enthusiast at a similar price. The R15 V3 promises to reduce that gap significantly with technology. On the list are an engine with a few more ccs, but variable valve timing takes the maximum power output to nearly 19bhp with a nominal decrease in maximum torque while spreading that torque over a wider rev range. It also gets all-LED lamps and an all-digital LCD instrument cluster that displays a wealth of information, including when the Variable Valve Actuation switches to the different camshaft profile. The global model also gets goodies like upside down forks, but the model in India gets regular forks. The pillion seat seems to be a little lower, at least visually, which should address some of the pillion complaints. The rear side panels also have a very Ducati Panigale-style gap between them and the rear subframe, while also neatly integrating the grab handles for the pillion, which the V2 lacked. There is no ABS available even as an option, and will be available only when it becomes mandatory in 2019. On display at the 2018 Auto Expo also was a ‘Daytona’ kitted R15, suggesting that like the previous versions, more performance will be made available via a kit at a later date.The R15 V3 competes against the KTM RC200 in part, but as always, it occupies its own niche in the Indian market, as the sportiest 150cc of them all.<br><br>

