		 <font color="black" size="4px">
		 It's been a month since the Honda Grazia has been with me. With over 3000kms done, the scooter has been a part of my daily life, shopping for groceries, riding to work or taking the lady on dates. In this report we will see how Honda’s premium scooter fared in everyday use. The 125cc motor may be not as quick as its rivals but the engine reflects Honda's famed refinement even when clocking a top speed of 94kmph (on speedo). Zipping through everyday traffic wasn't a task either; the Grazia felt nimble through choc-a-block situations. The 10.54Nm of torque was enough to pull me past the inevitable ‘aaram aat’ commuter along the way.I had to take it out grocery shopping with mom sitting one-sided, rather uncomfortably. The Honda Grazia misses out on a rear footboard as standard; something which is rather precious to Indian women sitting pillion. The manufacturer offers a perimeter guard as an extra which includes the footboard.On the other hand, the front footboard encouragingly accommodated two large supermarket bags the last time I was told to make myself useful at home. The hook under the seat hung a few knick knacks as well.The 18-litre underseat storage is large although the seat unlock switch near the ignition key slot was a tad disappointing. Even though the feature might seem different and useful, operating it is a task at times. A simple turn of the key to pop up the boot would be much easier.While we’ve seen how the Grazia behaves in city riding, I can’t wait to take the scooter for some fun riding around twisty roads. Stay tuned for our next report! 
