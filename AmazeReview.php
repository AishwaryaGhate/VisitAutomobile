		 	Why I would buy it: Upmarket looks, fresh cabin styling, more feature-ridden, easy of driving (CVT), solid warranty.<br>Why I would avoid it:Petrol engine does not sound refined, rear headroom cramped for tall occupants, if seating three at the rear is important to you.When it comes to compact sedans, its Honda’s turn to flex its muscle with its all-new Amaze that’s slated to be launched on 16 May.<br> This car has modern looks, fresh interiors with more features, a new lighter platform with better dissipation for rear and side collision, and the addition of a diesel CVT gearbox. Since we’ve already covered the diesels in our previous review, we will now focus on the petrol twins.Let’s take it for granted that you’re reading this review since a petrol fits your bill, be it the manual or the CVT. So refinement is up there in the list of expectations. However, what you need to know is that Honda could have strengthened the noise insulation jobs on the petrol to make it sound more refined and less coarse. And the rear head room can get cramped for tall passengers.Nevertheless, what goes in the favour of the petrol Amaze’s favour is the crisp gearbox shifting action with a light clutch pedal (manual), the ease of driving offered by an automatic transmission (CVT), a new lighter platform with more resistance to collisions, an upmarket look with more relevant features, and a better ride quality. Plus, Honda is betting hard on the standard three years ‘unlimited’ km warranty, fourth and fifth year ‘unlimited’ km extended warranty, a service interval that spans 1 year/10,000km and the annual maintenance package AMC that costs just Rs 3500 for the petrol.
