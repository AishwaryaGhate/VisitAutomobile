         	Why I would buy it:Better NVH characteristics, all-new looks, fresh interior design, additional features, easy to drive (CVT) <br>Why I would avoid it:If you are looking for comfortable rear seating for three, cramped rear headroom.<br>Honda is ready to induct its all-new Amaze into the Indian compact sedan segment on 16 May. In case you didn’t know already, the second-generation Honda Amaze is based on a new platform. However, the talking point of this iteration definitely has to be the addition of a CVT gearbox on the diesel variant from a mainstream manufacturer. This essentially means that the diesel and petrol mills will now be available with both the manual and automatic transmission.When it comes to appearance, the Amaze is an amalgamation of design cues from Honda’s own CR-V, Civic and City. So, the more angular headlamp design, chrome intense grille and tail lights may ring a few bells. But having said that, there’s no doubt that the overall design is far more sophisticated than the outgoing model. This is essentially due to a more flowing shape, snazzy nose, prominent shoulder crease, smart alloys and a higher bonnet-line. But, it still carries a chopped-off look especially on the front end.What marks the all new Honda Amaze down is the slightly rubbery diesel gearshift, a tad heavier clutch pedal (over the petrol), cramped rear head room for tall occupants. What helps the all-new Honda Amaze claw back into the segment are its desirable looks (pun intended), an updated feature list, a new platform with larger dimensions and a more sorted ride. The highlight is invariably its attractive looks and availability of the new CVT gearbox version on the diesel variant. The 2018 Amaze makes sense to those who are looking for a safe and spacious compact sedan that looks contemporary, is feature laden, easy to drive, and offers one the peace of mind involved in owning a Honda. We also feel that the price of the all new Honda Amaze will be crucial to its success, but we’ll just have to wait it out till mid-May to find out.
