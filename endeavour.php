The Ford Everest, also known as the Ford Endeavour in the Indian market, is a mid-sized to semi-full sized sport utility vehicle (SUV) produced by Ford since 2003 over three generations. The Everest is a five-door SUV variant of the Ford Ranger four-door pickup truck.<br><br>
 <font color="red">First generation (2003–2006): </font> based on the 1998 to 2006 Ford Ranger, which in turn derives from the 1998 to 2006 Mazda B-Series.<br><br>
 <font color="red">Second generation (2006–2015): </font> based on the 2006 to 2011 Ford Ranger, which in turn derives from the 2006 to 2011 Mazda BT-50, this model received a facelift in 2009.<br><br>
 <font color="red">Third generation (2015–present):</font> based on the Ford Ranger (T6), and designed in Australia, but built in Thailand, available from 2015.Ford unveiled the MY19 Ford Everest in May 2018. Changes include the new 2.0L Bi-Turbo I4 Diesel engine paired to the 10-speed Automatic transmission. Other changes include Autonomous Emergency Braking, a standard power liftgate on Trend models, new wheels, Sync 3 now standard on Ambiente models. Interior changes include more soft touch materials, 'ebony' dark colour scheme, and Active Noise Cancellation (ANC).<br><br>
