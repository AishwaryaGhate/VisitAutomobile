		 The Honda Dio received a facelift for 2017. Apart from a BS IV-compliant engine, the new Dio gets a slew of cosmetic updates and new paint schemes to boost its visual appeal.Cosmetic upgrades for the new Honda Dio include two white position lamps on the front apron and an LED strip on the handlebar. The rear section also gets a re-designed tail lamp, although it isn’t an LED unit. A wider seat makes it more comfortable for the rider and the pillion, while a mobile charging point under the seat improves convenience. The Dio also gets four new dual-tone paint schemes with new decals to improve the visual appeal. Auto headlamp on (AHO) feature is also offered. A ‘DLX’ variant has been added to the 2018 Dio range. It offers three new paint schemes, an LED headlamp, and a part-digital instrument cluster.Powering the Honda Dio is the same 109cc single-cylinder engine that delivers 8bhp and 9Nm of torque through a CVT transmission. The Dio rides on a bottom-linked front suspension and a rear monoshock. The 10-inch wheels of the Dio are shod with tubeless tyres. The drum brakes at the front and the rear are offered with Honda’s proprietary combi braking system.The new Honda Dio is available in nine paint schemes. It competes with other scooters like the Yamaha Ray ZR, TVS Scooty Zest and the Suzuki Let’s. The Honda Dio is priced at Rs 50,000 (ex-showroom Delhi) with the DLX variant costing Rs 3000 more. 
