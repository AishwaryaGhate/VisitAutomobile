		 	Chevrolet now has another facelift of its flagship sedan in the country – the Cruze. Like the older facelifts, this one again gets a few nips and tucks on the outside to extend its appeal.Exterior The Chevrolet Cruze has been powerful and dynamic in its looks. The large hexagonal two-piece grille with the bow-tie in the centre has been retained and gets black horizontal slats and a chrome periphery. The signature sweptback headlamps continue to be at the front while the fog lamp housing is new. It gets a chrome surround with LED daytime running lamps embedded in it. The side profile and the tail remain identical.Interior Tech fans will be delighted to know that the Cruze features a new standard MyLink display. This seven-inch touch screen is compatible with both Android Auto and Apple CarPlay. Other features include dual airbags at the front, side airbags, ABS, electric sunroof, 6-way power adjustable driver seat, rain-sensing wipers and keyless start. The look and feel of the car remain entirely the same from the inside with the addition of factory -tinted glasses to save you from the sun.Engine and performance Technically, the car is the same. It gets the same old 2.0-litre four cylinder turbocharged diesel engine. The VCDi engine develops 166bhp of power and 380Nm of torque that drives the front wheels. The transmission duties are performed by a six-speed manual gearbox or a six-speed torque converter automatic transmission.Price and Competition The Chevrolet Cruze is a D-segment sedan and competes with the modern looking Hyundai Elantra, the Skoda Octavia, Volkswagen Jetta and the Toyota Corolla Altis.
