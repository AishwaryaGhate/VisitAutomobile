	    <font color="black" size="5px">The Hyundai Elantra , or Hyundai Avante  in South Korea, is a compact car produced by the South Korean manufacturer Hyundai since 1990. It is now in its sixth generation.The Elantra was initially marketed as the Lantra in Australia and some European markets. In Australia, this was due to the similarly named Mitsubishi Magna Elante model.This gave rise to disagreement with other motor manufacturers, and the name was standardized as "Elantra" worldwide in 2001 (except in Korea).<br><br>
<font color="red">First Generation </font>:Launched in October 1990, the Elantra (codename J1) received a mid-term facelift in 1993.It was sold in Europe from the spring of 1991. It replaced the somewhat larger on the outside Stellar, although that model had not been offered in many markets. The Elantra competed with the likes of the Ford Sierra and Vauxhall Cavalier/Opel Vectra, but at a considerably lower price.The Elantra was powered by a Mitsubishi-designed 1.6 L (1595 cc) straight-four. This DOHC 16-valve 1.6 L unit produced 113 hp (84 kW) at 6000 rpm and could push the Elantra to 60 mph (97 km/h) in 9.5 seconds. The quarter-mile (0.4 km) run took 17.1 seconds and produced 80 mph (129 km/h). Top speed was 116 mph (187 km/h). The Elantra got 22 mpg‑US (11 L/100 km) in the city cycle. Starting in 1993 a Mitsubishi-designed 1.8 L (1836 cc) inline-four option was available; this unit produces 124 hp (99 kW) at 6000 rpm.<br><br>

<font color="red">Second Generation </font>:Launched in 1995, the second generation (codename RD or J2) was offered as a sedan and station wagon. It was sold in the South Korean market as the "Hyundai Avante" in sedan form and "Avante Touring" in the wagon body style. Some export markets such as Australia and Europe received the series as the "Hyundai Lantra" as per the first generation. Australian market wagons were titled "Lantra Sportswagon". Between 1996 and 1998, the Elantra was marketed as the "Bimantara Nenggala" (1.6-liter petrol) in Indonesia.In Europe, 1996 through 1997 sedan models had carried a silver Mercury-type false grille, while the wagon carried no front grille.<br><br>

<font color="red">Third Generation </font>:An all-new model (codename XD) was launched in 2000. The estate was dropped in favor of a five-door hatchback. Starting in 2001, all American models came with standard front and front-side airbags, air conditioning, power locks, power windows and power steering. This simplified dealer inventories and repairs and also sought to improve Hyundai's image of "value" cars.In 2003, all models were refreshed (codename XD2); this introduced new headlights and taillights, a new grille, new front and rear bumpers, a refreshed hood and trunk, and a redesigned dashboard.
The GLS trim is standard for the Elantra. The top-of-the-line model, the GT trim, came with a stiffer suspension, leather seats, fog lights, alloy wheels, lip spoiler and blue-lit instrument cluster.<br><br>

<font color="red">Fourth Generation </font>:A redesigned sedan (designated HD) debuted at the 2006 New York Auto Show for the 2007 model year. As before, the HD sold under the Hyundai Avante name in its South Korean home market. It utilized a resurrected appearance from the 1960s and 1970s called "coke bottle styling".The engine lineup included 1.6- Gamma and 2.0-liter Beta II gasoline inline-four engines, and a 1.6-liter turbodiesel inline-four. All engines featured improved fuel economy. A five-speed manual transmission was standard with an optional four-speed automatic.According to the Insurance Institute for Highway Safety (IIHS) the Elantra received a Good overall score in the frontal crash test and a Marginal overall score in the side impact test, but all Hyundai Elantras manufactured after November 2009 earned a Good overall score in the side impact test.Standard side airbags included front and rear head curtain airbags and front seat-mounted torso airbags.  .<br><br>

<font color="red">Fifth Generation </font>:The fifth-generation Avante debuted at the 2010 Busan International Motor Show in April 2010. It was codenamed "MD" for the sedan,"UD" for sedans manufactured in the US, and "JK" for the coupe. It featured a 1.6-L gasoline direct-injection engine, producing 103 kW (138 hp) and 167 Nm (123 lb-ft) torque, mated to a new 6-speed automatic or manual transmission. For markets such as the Middle East, the 1.6-L engine was a MPI version that delivers 128 hp. The Israeli market received the Elantra, with a 1.6 GDI 132 hp coupled to a 6-speed automatic transmission only. The design continued Hyundai's "fluidic sculpture" styling theme first seen in the 2011 Sonata. The new model went on sale in August 2010 in South Korea, and began selling between end of 2010 and early 2011 as a 2011 mode.<br><br>

<font color="red">Sixith Generation </font>:Following the September 2015 launch of the Avante in South Korea, the sixth generation Elantra was announced to be released for sale in February 2016 for the 2017 model year in North America.Two trims are present in the 2017 model release: SE and Limited (Value Edition and Sport released later). In India, the AD series Hyundai Elantra launched on 23 August 2016 with petrol and diesel engine options. Its petrol variants displaces 2.0 liters, while the diesel engine is a 1.6-litre unit.The design of the car has been changed to a more conservative appearance. The "fluidic sculpture" design that persisted across the Hyundai portfolio since 2011 is gone in the sixth generation Elantra. The car is now more like a fastback with its roofline sloped from the windshield to the rear of the car and it has less curves overall with pentagonal head and tail lights, a hexagonal grille, and redesigned body panels and bumper emphasizing straight lines along the body. The windshield is drawn back from the hood further than the prior generation, making for a more traditional sedan look.<br><br>

