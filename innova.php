<font color="red">Second generation </font> The second generation Innova, the AN140 series, was launched on 23 November 2015 in Jakarta, Indonesia. The new series is a completely new vehicle with a new platform and new diesel engines.[8] 2.0- and 2.4-diesel engines are carried over from the previous model, while 2.4- diesel engine are completely new. The available transmission options are five-speed manual and six-speed automatic.<br><br>
