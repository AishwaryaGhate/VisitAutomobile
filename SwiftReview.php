		 	The all-important question of ‘kitna deti hai highway pe’ has been answered and it looks quite impressive. In real world conditions and with two different drivers and varied road conditions, the Swift with its five-speed automated manual transmission was able to give us an efficiency of 20.4kmpl. What’s more even after knocking off 498kms, we still had a quarter of a tank and a possible range of up to 116km giving us a total of 614km.Now, the car has a 37-litre tank and an ARAI F.E figure of 28.4kmpl which would put the total theoretical range at 1050km, a mighty impressive figure but a bit hard to achieve. We feel that our 614km figure is equally impressive when you consider that this is in real world conditions. However, given that this is a diesel variant, we wouldn’t recommend that you push your luck with regard to how far you can go in terms of distance and you should tank up if your DTE readout drops to under 50km.On both journeys, the car clocked over 1000km. There were no issues with regard to seating as all the occupants were very comfortable. The well contoured seats provided enough support all around while the pliant ride meant, the car took most of rutted rural roads without losing its composure too much.Added to this mix was the fact that my family had recently brought in a four-legged member to the troupe. Ray really enjoyed his first big car journey and this has lead me to believe that he definitely gives a paws-up! To the Swift as a highway companion.
