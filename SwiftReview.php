         	A practical car is the one which is most convenient to travel and live with. And there are very few other cars which can beat Swift in this parameter. The Swift always had the right ergonomics. It was designed with a typical Indian usage in mind. And now in its third act, Maruti has also ironed out all the issues of the previous two generations. The Swift feels like it was designed to be a daily commuter in the first place. Its dimensions are perfect for zipping around effortlessly in the traffic. The balanced steering also helps in the city commute. Here we have the ZDi AMT trim with us and the two-paddle set-up instantly takes away all the driving stress in the city’s bumper to bumper traffic. The AMT lag takes some time to get used to, though. It shifts at around 2000-2200 rpm to a moderate throttle and if you input it correctly, the lag can be minimised. However, like all AMTs, if you floor the pedal, the transmission feels slightly sluggish, but that rarely happens in everyday driving.The Swift passes our rigorous five labour test with flying colours. It has proved why it has remained India’s favourite hatchback, especially in a cutthroat market like ours. The Swift is a family car, a multi-tasker that can be your daily driver, your weekend companion and at times it can double up as your fun car as well. The addition of AMT has made it even more convenient for a day-to-day usage. Backed up by Maruti’s reliability and unparalleled service network, the Swift continues to be the formidable champion in its segment. 
