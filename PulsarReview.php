		 <font color="black" size="4px">
		 	The quality of the Pulsar RS200 is way better than what we’ve seen in other Bajajs. The paint and the switchgear quality, the plastics used all around the bike is pretty good. What I didn’t like is the plastic around the cluster – it feels and looks little cheap. Operating the switchgear feels good on the Pulsar RS200 – there’s a sense of little premiumness to it. The Apache RTR 200 doesn’t disappoint here. Its quality levels is on par with the Pulsar RS200 and has the best paint quality. The matte finish on the Apache is excellent. The Pulsar RS200 is also equipped with the DRLs, instrument cluster that has side stand and service indicators. Fuel injection is standard, just like projector headlamps, back light on the switchgear which acts like a feel good factor. The bike is also equipped with LED turn indicators and taillight – same as the Apache 200. The Pulsar RS200 has an edge over the Apache in the gearbox department as the former gets a six-speed whereas the latter is equipped with a five-speed transmission. I usually desire things that are popular and in this case, it’s the Pulsar RS200. Since its birth, the Pulsar had a crazy fan following, not because it is a Bajaj bike but because this brand was always linked to performance. And performance is usually associated with coolness and style in case of the Indian youth, so in terms of desirability the Pulsar RS200 is a clear winner here.
