	    <font color="black" size="5px">Given the way the premium 125cc scooter market is catching pace, it is not a shocker then that Suzuki decided to try and carve out a share of this segment. The Japanese manufacturer unveiled an upgraded version of the Suzuki Access at the 2016 Auto Expo, last week, called the Access 125. This scooter has been given a new lease of life thanks to a new 125cc heart and a slew of cosmetic updates aimed at giving it a retro flavour.The Suzuki Access 125 gets a completely new fascia thanks to the redesigned apron and rectagular-ish headlamp. The side profile has also been revised thanks to reshaped curvy body panels, which now adorn a 3D emblem. The subtle touches of chrome on the headlamp and the exhaust shield give it a premium feel. The large silver-finished grab rail and the black plastic panels beautifully contrast with the scooter's glossy finish. The instrument cluster design is also new, and now integrates a digital multi information display. The large seat should make it a very comfortable scooter to ride, while realtively low seat height of 780mm makes it accesible even for short riders. It also gets a DC power socket, to plug in your cell phone charger.While the Indian two-wheeler market has now gotten used to mildly revised models being called as 'all-new', Suzuki for once has actually gone ahead and comprehensively upgraded this product. The revised styling in itself give the scooter a different identity, though there is more to it than just some cosmetic changes. Under the skin, Suzuki says that the Access 125 has been built around a new platform. The Access 125 is powered by a new 125cc single-cylinder engine, which delivers 8.7bhp and 10Nm of torque through a CVT transmission. The engine comes with the company's proprietary Suzuki Eco Performance (SEP) technology. This technology reduces all kinds of mechanical losses in the engine, thereby improving the efficiency of the scooter without compromising the performance.<br>