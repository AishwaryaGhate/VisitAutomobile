		 <font color="black" size="4px">
		 	The Access 125 has been the most successful product for Suzuki in the Indian market. Since its introduction in 2007, it has been a money-spinner for the company. The reason being, it offered the right amount of value for the money and in fact, it was priced on par with certain 110cc scooters. However, after the launch of the Honda Activa 125 in 2014, the sales of Access 125 started to drop, calling for a much needed upgrade and now we’ve one in 2016 and it’s more than just a cosmetic update. So does the new Access 125 have what it takes to command the numero uno position in the 125cc segment once again? Here’s what a spin on the scooter told us.Open the throttle and you quickly realise the amount of work has gone into the new Access 125. Thanks to the new lighter chassis, the Access 125 is too eager to roll off the start. The kerb weight of 102kg ensures that the scooter is easily manoeuverable in tight spots and it also holds the title of being the lightest vehicle in the 125cc segment.  The handlebar position is slightly taller than the other scooters, which makes life easy for tall riders like me. The bar doesn’t touch the knees every time you take a U-turn and truly aids city driving a lot, especially low-speed riding.Excellent is how I would describe the ride quality.  The suspension setup of this scooter swallows all kinds of potholes, bumps and undulations effortlessly.The setup is on the softer side, and is not frazzled by any kind of weight. The 12-inch wheel in the front, is pretty useful in this scenario. What further works in its favour is the comfortable single seat and highway manners which charm you right off.If you’re in the market for a 125cc scooter, you’ve two more options – the Honda Activa 125 which is two years old and is the most expensive in the present range or the Mahindra Gusto 125, which behaves like an 110cc scooter, plus, it hasn’t been launched in most of the Indian states.
