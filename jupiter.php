	    <font color="black" size="5px">This is a no brainer to identifying scooters in India – if it is small, multi-coloured and lightweight, then it’s a ‘Scooty’; and if it is big, dull and utilitarian then it is the ‘Activa’.  Everything else sold in these segments are looked at as if they are just other versions.The new TVS Jupiter faces the same issue in the 110cc segment where it takes-on the Honda Activa. The only advantage it has is TVS’ past experience in scooter market. The Wego is a brilliant scooter and the Jupiter uses the same platform with few other additions that should make it more potent.We spent some time with the TVS Jupiter trying to figure out if it had enough to topple the Activa or is it just another addition to the already overcrowded segment.The TVS Jupiter is a contemporary looking scooter with a perfect balance of simplistic design and sharp styling. It is big and almost on par with Honda Activa in size, but thankfully is lot more pleasing to the eyes. Also the design from no angle is feminine and so the Jupiter should not be typecast like its ‘Scooty’ badge.The front of the Jupiter has vertical creases, which along with the turn-blinkers lend it a very sleek look. The triangular air vent is a bit quirky, but generally goes unnoticed in the bigger picture. The trapezoidal headlamp is big and does more than a decent job at night.The side profile of the Jupiter is a bit disappointing – not that it has any serious design flaws, but it is just too ordinary. Coming to the rear, the first thing you will notice is the fuel filler cap – just like for the Wego, TVS has given it an external fuel filler cap that is convenient. The angular tail lamp gets LED lights and is surrounded by similarly shaped clear lens indicators. The silver coloured grab rail is big and utilitarian, but somehow reminds me of the one on the Honda Activa.<br><br>

