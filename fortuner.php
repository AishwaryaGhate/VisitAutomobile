	    <font color="black" size="5px">
	    The Toyota Fortuner also known as the Toyota SW4, is a mid-size SUV manufactured by Toyota. Originally assembled only in Thailand but later also in Indonesia and other countries, the Fortuner is built on the Toyota Hilux pickup truck platform. It features three rows of seats and is available in rear-wheel drive or four-wheel drive. The Fortuner is part of Toyota's IMV project in Thailand, which also includes the Toyota Hilux and the Kijang Innova (in Indonesia). Developed in large part by Toyotaâ€™s Thai operations, the Fortuner has piggybacked the success of the Hilux and is now built in a number of countries including Egypt, India, Indonesia, Argentina and Pakistan.  <br><br>
<font color="red">First Generation </font> The first generation Fortuner was unveiled at the December 2004 Thailand International Motor Expo and released in early 2005. It is positioned below the Land Cruiser and above the RAV4. Model codes included: TGN50, GGN60, TGN60, KUN60 Initially the Fortuner was sold with 4 different types of 2 petrol engines and 2 diesel engines. All variants were offered in RWD or 4WD configuration, with 5-speed manual transmission and a 4 or 5-speed automatic transmission. Its high body model has been criticized as unstable, and not aerodynamic when running at higher speeds. In August 2012, the 2.5 L common-rail turbodiesel 2KD-FTV engine was upgraded to a variable nozzle (VNT) turbocharger which was equipped in the 3.0 L common-rail turbo diesel 1KD-FTV since its release in 2005 to increase power and torque about 60%, also reducing fuel consumption up to 30%.  <br><br>

<font color="red">Second Generation </font> The second generation Fortuner was revealed simultaneously on 16 July 2015 in Australia and Thailand. Along with the Toyota Hilux, the Fortuner uses the "Keen Look" design language that has already been used in the Toyota Corolla (E170) with slim projector headlights and LED DRL (Daytime Running Lights). But this time, Toyota designed the Fortuner's interior to be different from Hilux, to give a distinct character between them.Both of them shared the chassis, transmission and engine lineups, with two new diesel engine (1GD & 2GD), also 2.7L and 4.0L petrol engine was updated with Dual VVT-i. This generation Fortuner features a part-time 4WD system instead of the full-time 4WD system used in the previous generation<br><br>
