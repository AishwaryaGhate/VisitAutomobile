	    <font color="black" size="5px">The Suzuki Ciaz (also known as Suzuki Alivio in China) is a compact car produced by Suzuki, developed to replace the Suzuki SX4 Sedan in Asian and Latin American markets.The Suzuki Ciaz is available in both diesel and petrol variants. It is being offered with a 1.4 litre K-Series petrol engine and a 1.6 litre VVT exclusively for the Chinese market and Fiat's 1.3 litre diesel engine. The diesel-engined SHVS model had a combustible comsumption rate is above 28.09  km/L while the fuel version comsumption average is 20.73 km/L according to ARAI.The power (BHP) rating will be 91.18 (Petrol) and 88.76 (Diesel). The torque (NM) will be 130 (Petrol) and 200 (Diesel). The Car's petrol version will also have an automatic transmission option, the same currently used in the SX4 Automatic.
It comes with features like projector headlamps, new 16 inch alloy wheels, key-less entry, window blinds, a touchscreen infotainment system, rear parking camera, automatic climate control, ABS with EBD and two airbags, rear AC vents, push button stop-start system, etc. The Chinese Version of Ciaz; known under the Alivio name; has sunroof as an extra feature.<br><br>
<font color="red">Dimensions:</font>Suzuki Ciaz is 4,490 mm in length, 1,730 mm in width, 1,485 mm in height. The ground clearance of the sedan is 170 mm and wheelbase is 2650mm. The boot space of 510-litres and is the largest in its class.
 <br><br>
<font color="red">Safety Features:</font>The Suzuki Ciaz comes with ABS (Anti-lock Braking System) and EBD (Electronic Brake-force Distribution) on all variants. It comes with Dual Airbags across all variants. The car gets a reverse parking sensors in Delta variant while reverse camera is available in Zeta and Alpha variants. The car was also tested by ASEAN NCAP and got a 4-star rating.<br><br>
<font color="red">Engine And Performance:</font>However, there are minor changes on the mechanical front as the new version comes with the same 1.4L petrol and a new 1.3L diesel-hybrid engine. Also, a new 4-speed automatic transmission gearbox unit has joined the lineup of manual transmission options for certain top-end petrol variants.The 1.4-litre K14B petrol engine makes a 91.1 bhp power @ 6,000 RPM and 130 Nm torque @ 4,000 RPM, while the 1.3-litre DDiS diesel engine makes 88 bhp power and 200 Nm torque. Maruti Suzuki claims that the Ciaz petrol delivers a fuel efficiency of 15-16 km per litre, while the diesel trim returns around 25 km per litre mileage on city road conditions.
The Ciaz sedan will be removed from regular dealerships and the sales will be pushed exclusively to the premium Nexa dealerships, which presently sells S-Cross, Baleno, Ignis and Baleno RS.<br><br>
<font color="red">Ciaz Price:</font>Maruti Suzuki Ciaz price starts at ₹ 8.04 Lakhs and goes upto ₹ 11.86 Lakhs. Petrol Ciaz price starts at ₹ 8.04 Lakhs. Diesel Ciaz price starts at ₹ 9.71 Lakhs.

