		 <font color="black" size="4px">
		 	If you have read our launch ride and track-day stories, you must be aware of the Yamaha YZF-R15 V3.0’s superiority on the track. But what about the real world, where it will spend most of its life? How does the R15 perform as an everyday ride? Back at the track, the R15 might have paled in comparison with the Ducati. On the street however, it is a completely different story. You can only appreciate the full drama of the R15’s design when it is in town, surrounded by commuter bikes and scooters. The Indian audience has had a long standing relationship with faired motorcycles, and the R15 grabs eyeballs everywhere it goes.The R15 was a benchmark when it came to quality, but that is not the case anymore. With the V3.0, there has been a marked drop in the quality of plastic components. There are hardly any panel gaps and the motorcycle feels well put together, but the quality of certain fibre panels has taken a hit. The switchgear has a nice tactile feel to it, but the button on the instrument console requires a lot of effort to operate. This can get annoying when you try to operate it on the move. Unlike the previous R15s, the new bike gets a fibre fuel tank, which means I cannot use magnetic tank bags.The R15 is a lot more than a poser’s bike, it is a stepping stone into performance motorcycling. It has the traits of a good commuter bike bundled with modern technology and the fun aspect of a supersport bike. And with a price tag of less than Rs 1.5 lakhs, it is within the reach of masses. It does all of this without letting the rising fuel costs play spoilsports. Sure, your back will hurt like hell, but that is just another motivation for you to work on your physical fitness.At Rs 1.26 lakhs (ex-showroom), the R15 is pricey for a 155cc bike. You can land yourself a quarter-litre Yamaha for the same money. But there is no way you will be having the same amount of fun on the FZ25. The suspension setup, perimeter frame, aluminium swingarm and the sticky tyres make for one of the sweetest handling packages. We knew it was a great track tool, but now it has proven to be a good streetbike as well.
