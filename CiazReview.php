		 	Why I would buy it Great value, Loads of space inside the cabin, Well-balanced looks Why I would avoid it 4-speed automatic is slow to react and is simply outdated, Steering feels too vague Ever since it was introduced in late 2014, the Maruti Suzuki Ciaz has been a prevailing choice for those after a sensible and spacious sedan in the C-segment. It pulls in big numbers, too, often outselling the likes of Hyundai Verna and Honda City. Despite the strong sales, though, we could never see it as a segment-leading choice, something that Maruti has tried to fix with this substantial update. Say hello to the 2018 Ciaz which is now on sale with prices ranging between Rs 8.19 lakhs and Rs 10.97 lakhs. Thanks to an all-new engine, the petrol-powered Ciaz makes more power and torque than its predecessor. It also has a new look and comes loaded with more safety tech. Here are our first impressions after a long drive in the garden city of Bangalore. While it’s no driver’s car nor the most desirable in its class, nothing else comes close to the 2018 Ciaz when it comes to space and comfort, and that’s where it shows its strongest dispute. If you are after willing performance or cutting-edge gearbox tech, you would be better off with a VW Vento TSI or the Honda City, but if you need a comfortable city runabout that’s great value, the noticeably cheaper 2018 Ciaz is hard to fault.The 2018 Ciaz range starts at Rs 8.19 lakhs for the base petrol manual version, going all the way to Rs 10.97 lakhs for the top-spec petrol automatic and diesel manual version. All its rivals including the Honda City, the Hyundai Verna and the Toyota Yaris are considerably more expensive. 
