	    <font color="black" size="5px">The Honda Amaze is a 4-door sedan produced by Honda. It is the sedan version of the Brio hatchback. It followed the same design as the Brio until 2018. Honda launched the Amaze in India in April 2013 and was developed at Honda R&D Asia Pacific Co., Ltd. located in Bangkok, Thailand. The Amaze is available in petrol and diesel engine.<br><br>
<font color="red">First Generation :</font>The Amaze is being manufactured at the company’s facility in Greater Noida, with a localization level of more than 90%.The Amaze is Honda's first diesel offering in India. The car is also available with the 1.2 L (73 cu in) L12B i-VTEC petrol engine that is used in the Fit/Jazz and Brio hatchbacks.Apart from being the company’s first diesel car in India, the Amaze is slotted in the lucrative sub 4-meter entry-level sedan segment, where it competes against the Maruti Swift DZire, Tata Indigo eCS, Tata Tigor, Volkswagen Ameo, Zest, Ford Figo Aspire and Hyundai Xcent.In the Philippines, the Brio Amaze also competes with the Mitsubishi Mirage G4.Honda provides a new India-specific 1.5 L (92 cu in) diesel engine in the Amaze. This engine gets the i-DTEC moniker and it is a derivative of the new 1.6 L (98 cu in) Civic diesel engine recently launched in Europe. This India-specific derivative was developed around excise/taxation rules which offer a favorable tax regime for cars of sub 4-meter cars with engines of less than 1.5 L (92 cu in) capacity. Honda claims this all-aluminum engine is the lightest diesel engine in its class.<br><br>

<font color="red">Second Generation :</font>The second generation Amaze was unveiled at the February 2018 Auto Expo in India, which Honda claims to be built on a new platform. The diesel engine is now mated to a CVT. The car has been developed at Honda R&D Asia Pacific Co. Ltd in Thailand. The second generation Amaze is offered in E, S, V and VX trims.In July 2018, Honda Cars India Ltd.(HCIL) recalled 7290 second generation Honda Amaze in regards to a potential issue with the electric power steering. Effected Amaze's were manufactured from 17 April 2018 to 24 May 2018.<br><br>

