		 <font color="black" size="4px">
		 We Indians love living an XL life. We love owning big houses, large phones, big dogs and despite our ever crowded streets we crave for large brawny SUVs. A year back if you wanted to buy a rugged SUV for around Rs 30 lakh, the Fortuner was your default choice. Toyota hit the sweet spot with its tough build, off-road capability, punchy engine, bullet proof reliability and minimal competition.Unlike the old car, the Endeavour looks well rounded and the tapering D pillar enhances the looks. The near 5-metre long profile is dominated by the flared wheel arches and the massive 18 inch wheels.The new Endeavour’s cabin feels three generations younger as compared to the old one. The design is modern and materials in the upper portion of the dash look really nice. The dash top is wrapped in quality leather and the chrome accents and grey finish help make the cabin lively. You also get seven airbags, ESP, traction control, hill launch assist and hill descent control.There’s a TFT touch-screen music system with 10 speakers and a Sync system with voice control along with the regulars.The ride is impressive too. The Endeavour absorbs everything our roads throw at it. Potholes are dispatched with distant, well-damped thunks and it manages to retain decent composure without feeling uncomfortable. If anything, it is that this absorbent ride leads to a bit of pitching at higher speed but by segment standards it isn’t half bad.The new Endeavour has definitely been worth the wait and it shines in many areas. It rides and handles well for a big SUV, it is spacious, comes loaded with features, and the interior quality is decent too. Where it lacks as compared to the rest of the car is in its engine and gearbox package. Though both motors aren’t underpowered, a better gearbox would have made it much more potent package.</font><br><br>
