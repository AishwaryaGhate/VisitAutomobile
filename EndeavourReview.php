         <font color="black" size="4px">
         	Priced at Rs 31.17 lakh (ex-showroom Mumbai) it is on the expensive side and will more often than not be purchased by those wanting something large, luxurious, fashionable, comfortable and have the ability to go to most places without much hesitation. It is this last feature that we wanted to explore as we wanted to know what it would be like to muscle such a huge vehicle on an off-road expedition for the average buyer. It would also give us a chance to fiddle with all the kit that Ford has fitted into the car with regard to making it a capable off-roader.Impressed so far with the ability of this massive creature, we decided to give the rock mode a try and went and found ourselves a rocky path. This would give us an idea of its ability to articulate over uneven surfaces. Now doing something like this is a bit extreme and as is the case with anything extreme, you have to prepare yourself. To activate the rock climbing mode, the four low has to be engaged and only then you can enter the rock climbing mode.As the sun set and the plummeting temperature began to attack us, we bade farewell to 19 Degree North and climbed down Aamby Valley. Out on the Mumbai-Pune Expressway, the Endeavour returned to being a quiet and comfortable mile muncher letting me reflect on our day out. I had a blast with the SUV and would definitely recommend it if you plan to do some off-roading once in a while.
