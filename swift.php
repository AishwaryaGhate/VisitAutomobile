	    <font color="black" size="5px">The Suzuki Swift is a subcompact car produced by Suzuki. The Suzuki Swift is classified in B-segment in European single market, segment known as Supermini in British islands. Prior to this, the "Swift" nameplate had been applied to the Suzuki Cultus in numerous export markets.The Suzuki Swift began in 1983 as a marketing and manufacturing rebadge of the Suzuki Cultus, a supermini (or subcompact) manufactured and marketed worldwide across three generations and four body configurations—three-door hatchback, four-door sedan, five-door hatchback and two-door convertible—and using the Suzuki G engine family.The Swift was marketed in the Japanese domestic market (JDM) as the Cultus and elsewhere as the Suzuki Swift, Suzuki Forsa, Chevrolet Swift, Chevrolet Sprint and Sprint Metro, Geo and Chevrolet Metro, Pontiac Firefly, Maruti 1000, Holden Barina and Subaru Justy. Versions of the second generation Cultus were also produced until 2007 in India and the car remains in production today in Pakistan and China. For more information on the initial versions of the Swift, see: Suzuki Cultus and Geo Metro. <br><br>
<font color="red">First Generation :</font>The first generation Swift was introduced in 2000 as a replacement for the Suzuki Cultus. Outside Japan, the "Suzuki Ignis" name was used. Both three- and five-door hatchback body styles were offered, although the three-door was not offered as part of the regular lineup in Japan.The Swift was powered by a new generation of Suzuki inline-four gasoline engines, the M family. Engine displacements of 1.3 and 1.5-litres were offered, both with a five-speed manual transmission or optional four-speed automatic. The vehicle was available with either front or four-wheel drive. Vehicles fitted with the 1.3-litre engine were designated HT51S, with the 1.5-litre version assigned HT81S.
The shorter three-door body variant formed the basis of the Swift Sport in Japan, or Ignis Sport in export markets. Introduced in 2003, it featured redesigned bumpers, and was fitted with a higher-output version of the 1.5-litre engine. The Sport ceased production in 2005, with the regular Swift models remaining until 2006 for the Japanese market only.<br><br>

<font color="red">Second Generation :</font>The second generation Swift debuted at the Paris Motor Show in September 2004. The design of the new Swift was previewed on the Concept S and Concept S2 concept cars at auto shows, in the years leading up to its launch. This generation of the Swift marked a significant departure with the previous Cultus-based models, with Suzuki redesigning the vehicle as less of a "low price alternative" subcompact and more of a "sporty" subcompact. The Swift's design and driving characteristics focused on the European market with its chassis refined through a road-testing program across Europe.The second generation Swift has received a four stars out of five rating in the Euro NCAP crash tests.It also was awarded 2006 Semperit Irish Car of the Year in Ireland.Since its global launch in 2005, which was kicked off with a marketing campaign fronted by the footballer Cristiano Ronaldo in many European countries, the Swift has recorded above forecast sales in most markets.<br><br>

<font color="red">Third Generation :</font>The third generation Swift is being produced at Suzuki's plant in Hungary from where it will be supplied across Europe. The new car has its wheelbase extended by 50 millimeters over the previous second generation and has many visual updates. While the new car looks different from the old one, its design is an evolution of the radical second generation styling with a longer and more rounded appearance. Post-September 2010 Swifts are fitted with a heavily revised version of the K12 engine found in the Suzuki Wagon R+d. During September 2010, Suzuki in the United Kingdom released the third generation Swift onto British roads.The new generation is expected to feature a 1.2-liter VVT petrol engine developing 92 bhp. In some countries, the 3rd generation of Suzuki Swift uses a 1.4-liter VVT petrol engine (K14B) which produces 95 hp.In Thailand, the Swift is built locally and is part of the Thai government's eco-car program. The car is sold with a CVT automatic transmission on GA, GL and GLX variants; GA and GL variants are also offered with a five-speed manual transmission without anti-lock brakes.<br><br>
